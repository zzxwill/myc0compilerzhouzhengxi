package generate;
public class gen_token{
	private int label;
	private String name;
	private int code;

	    public int getlabel() {
	        return label;
	    }

	    public String getname() {
	        return name;
	    }

	    public int getcode() {
	        return code;
	    }

	    public void setlabel(int label) {
	        this.label = label;
	    }

	    public void setname(String name) {
	        this.name = name;
	    }

	    public void setcode(int code) {
	        this.code = code;
	    }
	   
	}
